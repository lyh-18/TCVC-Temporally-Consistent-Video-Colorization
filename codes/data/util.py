def _read_img_lmdb(env, key, size):
    """read image from lmdb with key (w/ and w/o fixed size)
    size: (C, H, W) tuple"""
    with env.begin(write=False) as txn:
        buf = txn.get(key.encode('ascii'))
    img_flat = np.frombuffer(buf, dtype=np.uint8)
    C, H, W = size
    img = img_flat.reshape(H, W, C)
    return img


def read_img(env, path, size=None):
    """read image by skimage or from png
    return: Numpy int8, HWC, RGB, [0,255]"""
    if env is None:  # img
        img = cv2.imread(path, cv2.IMREAD_UNCHANGED)
    else:
        img = _read_img_lmdb(env, path, size)
    if img.ndim == 2:
        img = np.expand_dims(img, axis=2)
    elif img.ndim == 3:
        img = img[:, :, [2, 1, 0]]
        img = img.astype(np.float32)
    
    # some images have 4 channels
    if img.shape[2] > 3:
        img = img[:, :, :3]
    return img

def read_img_npy(env, path, size=None):
    """read image by skimage or from npy
    return: Numpy float32, HWC, BGR, [0,1]"""
    if env is None:  # img
        img = np.load(path)
    else:
        img = _read_img_lmdb(env, path, size)
    if img.ndim == 2:
        img = np.expand_dims(img, axis=2)
        
    # some images have 4 channels
    if img.shape[2] > 3:
        img = img[:, :, :3]
    return img

def read_img_lab_seq(path, color_type):
    """Read a sequence of images from a given folder path
    Args:
        path (list/str): list of image paths/image folder path

    Returns:
        imgs (Tensor): size (T, C, H, W), RGB, [0, 1]
    """
    if type(path) is list:
        img_path_list = path
    else:
        img_path_list = sorted(glob.glob(os.path.join(path, '*')))
    img_list = [read_img(None, v) for v in img_path_list]
    img_lab = channel_convert(img_list[0].shape[2], color_type, img_list)
    
    img_l = [v[:,:,:1] for v in img_lab]
    # stack to Torch tensor
    imgs_l = np.stack(img_l, axis=0)
    imgs_lab = np.stack(img_lab, axis=0)
    imgs_l = torch.from_numpy(np.ascontiguousarray(np.transpose(imgs_l, (0, 3, 1, 2)))).float()
    imgs_lab = torch.from_numpy(np.ascontiguousarray(np.transpose(imgs_lab, (0, 3, 1, 2)))).float()
    return imgs_l, imgs_lab

